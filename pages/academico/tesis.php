                                <div class="cell-lg-5">
                                  <p>Para graduarse del grado de Licenciatura en la Universidad Tecnológica Costarricense
existen DOS opciones; Pruebas de Grado o Tesis, en ambas se debe tener el Trabajo
Comunal Universitario finalizado y haber concluido y aprobado el Plan de Estudios de su
carrera.
<br>
<br>
En algunos Programas de Estudio se indica que la materia de Investigación Dirigida forma
parte del Plan de Estudios y en otros no; (verifique su plan) no obstante él o la estudiante
que realizará Tesis necesariamente debe contemplarla como requisito indispensable.
<br>
<br>
El curso de Investigación Dirigida, es de gran importancia pues desarrolla los conceptos
de la investigación científica aplicado a los proyectos de investigación.
<br>
<br>
Con esto se pretende que el estudiante esté en capacidad de realizar investigaciones,
analizar sus resultados y proponer alternativas de solución, además de adquirir:
 <ul class="list-group text-left">
                  <li class="list-group-item">✓ Dominio de los conceptos relacionados con la investigación científica</li>
                  <li class="list-group-item">✓ Analizar las partes fundamentales de un proceso de investigación.</li>
                  <li class="list-group-item">✓ Seleccionar un tema de investigación, acorde al problema planteado, esbozar la
estrategia para resolverlo.</li>
                </ul>
                 Se insiste que para desarrollar la tesis se debe llevar y aprobar la materia de
Investigación Dirigida, aunque el programa curricular de su carrera no lo incluya, en dicha
materia y con orientación del docente se deberá escoger un tema a desarrollar o
problema a estudiar y poder plantear alternativas de solución, la tesis debe ser orientada
directamente a la carrera en que se desenvolverá como profesional, no se trata de una
simple descripción de los hechos descubiertos en el proceso de investigación, sino,
respetando el método científico, determina que aportes o posibles soluciones se
presentan a ese tema o problema en cuestión.
<br>
<br>
Con el tema correctamente seleccionado por el estudiante, con la aprobación en primera
instancia del docente de Investigación Dirigida; se debe enviar una carta al Director de
Carrera solicitando la aprobación definitiva del tema, este documento lo realiza el
estudiante y debe entregarlo en el Departamento de Registro, anexando a la carta un
resumen ejecutivo que contenga al menos la justificación del problema y los objetivos
tanto general como específicos. El tiempo de entrega del tema debe de realizarse en la
semana 3 y 4 de cada inicio de cuatrimestre.
<br>
<br>
La semana siguiente de haber entregado original y copia en el Departamento de Registro,
los interesados deberán consultar por el resultado en el mismo Departamento.
<br>
<br>
Las tesis deben ser elaboradas de forma estrictamente individual; no obstante, en casos
muy excepcionales que, por la dificultad del tema, amerite dos proponentes se aceptaría
a criterio del Director de Carrera y el V°B° del Rector.
<br>
<br>
Una vez aprobado su tema de investigación se desarrollarán los primeros tres capítulos
de la tesis con la guía del docente de la materia de Investigación Dirigida, concluida la
materia en el cuatrimestre, el docente pasará el Acta de Notas del curso.( si el estudiante
no matrícula el siguiente cuatrimestre la tesis, deberá solicitar por escrito una prórroga,
donde indique porqué razón matriculará en otro periodo y su deseo en que se mantenga
el tema desarrollado en el curso de Investigación Dirigida).
