<p>Los estudiantes que continúan con tesis deberán realizar una segunda carta dirigida al
señor Director de Carrera, solicitando se le asigne un tutor o director especializado en el
tema a desarrollar para continuar con su trabajo final, se debe adjuntar un resumen
ejecutivo de lo actuado en los primeros tres capítulos realizados en las materias.<br><br>El estudiante puede sugerir un Director de Tesis, bien sea docente activo de la UTC o
externo para lo cual debe presentar el Currículo Vitae de forma electrónica.
<br>
<br>
De igual manera se entregará original y copia en Registro y a la semana siguiente, se le
informará al estudiante sobre el docente que estará a cargo de la dirección. Antes de
entregársele la carta donde indica el tutor asignado los estudiantes deben presentarse al
Departamento de Servicio al Cliente y matricular la tesis, esta tiene un costo determinado
y un tiempo perentorio para ser finalizado en cinco meses para su presentación, si por
alguna razón de peso y transcurrido ese tiempo usted no la ha terminado, deberá solicitar
por escrito a la Rectoría y con la venia del tutor o director de Tesis, tiempo extra que, de
ser autorizado, comprende un periodo máximo de dos meses, siempre y cuando lo
informe antes de finalizar el periodo regular otorgado y que fue especificado en la carta
de lo contrario deberá cancelar un arancel extra.</p>
                                  <p>Una vez realizado los trámites anteriores y recibida la carta en donde se le informa sobre
su tutor; se continúa la investigación la cuál debe finalizar con su defensa en el tiempo
indicado. (el tiempo del tutor corre a partir de que el estudiante matrícula la tesis).
<br>
<br>
Los estudiantes que opten por las Pruebas de Grado y que lleven la materia de
Investigación Dirigida deben preparar su investigación de tal manera que, al finalizar el
cuatrimestre termine su trabajo, el docente reportará la nota alcanzada en la materia. La
intencionalidad es que aprendan a desarrollar un trabajo de investigación científica que
le ayudaría mucho en su ejercicio profesional.
<br>
<br>
Para el grupo de estudiantes que aplicarán las Pruebas de Grado iniciarán al siguiente cuatrimestre (una vez finalizado su plan de estudios) donde realizarán cuatro pruebas, éstas se realizaran después de haber llevado un seminario,el cuál debe ser cancelado con 8 días de anticipación  (no es obligatorio por parte de la Universidad impartirlo ni del estudiante recibirlo). 
<br>
<br>
Se hará un examen teórico-práctico con el docente a cargo del seminario y un examen
oral con un jurado calificador, quien es nombrado por cada Dirección de Carrera de la
Universidad y compuesto por docentes de la Institución.
<br>
<br>
Al inicio de cada cuatrimestre se publican las fechas de inicio y finalización de cada una
de las pruebas, estas fechas comprenden un periodo en el cuál se debe iniciar y finalizar
cada una de ellas.
<br>
<br>
En resumen, el estudiante debe verificar su plan de estudios, presentar su TCU y luego
tomar la decisión si presentará Pruebas de Grado o realizará un Trabajo Final como
Tesis.
