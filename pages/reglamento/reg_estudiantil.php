        <h3>Capítulo V  -  Del Plan de Estudios.</h3>
                     <section class="section-60 section-sm-60 bg-lightest">
          <div class="shell text-sm-left">
            <div class="range range-xs-center">
            </div>
            
              <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 27</div>
                <div>
                  <h5 class="text-bold">Asesoría para trámites estudiantiles.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Al iniciar su carrera en la Universidad Tecnológica Costarricense,  el estudiante recibirá de los funcionarios del Departamento de Servicio al Cliente, la asesoría necesaria para proceder correctamente con los trámites administrativos.</p>
              </div>
            </div>
            
      <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 28</div>
                <div>
                  <h5 class="text-bold">Modificaciones al plan de estudios.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Todo estudiante inscrito en una carrera, está sujeto a modificaciones que se realicen al Plan de Estudios, siempre que no se trate de cursos o bloques de materias que ya aprobó o que esté cursando.  En caso de que el Plan de Estudios sea modificado,  el estudiante tiene derecho a permanecer con el Plan de Estudios vigente a la fecha de su matrícula original, siempre y cuando no haya tenido interrupciones superiores a dos períodos lectivos consecutivos o tres alternos.</p>
              </div>
            </div>   
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 29</div>
                <div>
                  <h5 class="text-bold">Reincorporación al plan vigente</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Si el estudiante se retira de la Universidad por más de dos cuatrimestres, en el momento de su reingreso deberá incorporarse al plan de estudios vigente a la hora de la reincorporación, en el caso de las materias que aún no haya cursado y aprobado.</p>
              </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 30</div>
                <div>
                  <h5 class="text-bold">Finalización del plan de estudios en licenciatura.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>El Plan de Estudios de licenciatura finaliza con la realización del Seminario de Investigación Dirigida, u otras modalidades aprobadas por CONESUP.
El objetivo de los seminarios o cursos es la realización de la Tesis, el Proyecto de Graduación, la Práctica Profesional o el Examen de Grado, o cualquier otra modalidad que constituye uno de los requisitos de graduación en ese nivel.
</p>
              </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 31</div>
                <div>
                  <h5 class="text-bold">Matrícula de cursos conducentes a trabajos finales de graduación.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Para la realización del Seminario de Investigación Dirigida, el estudiante, cuando así lo especifique el Programa de Estudios,  debe concluir los demás cursos del plan de estudios. De no exigirlo, el estudiante podrá cursar en el ciclo correspondiente, en forma simultánea al Seminario de Investigación Dirigida, las materias que indica el plan de estudio. </p>
              </div>
            </div>              
  <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 32</div>
                <div>
                  <h5 class="text-bold">Cursos de  práctica profesional</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Los cursos de prácticas profesionales que requieren las carreras del Área de Informática, deben matricularse como los demás cursos.  Los cursos se ganan con una nota no menor a 70.  En caso de aplazamiento, el estudiante debe matricular nuevamente la materia, la cual, en ningún caso se podrá presentar por suficiencia.</p>
              </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 33</div>
                <div>
                  <h5 class="text-bold">Exámenes colegiados</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>En materias que son comunes a varias carreras, y en las que existen varios grupos de estudiantes, el Director de Carrera, podrá programar exámenes colegiados que deben  realizarse en las fechas señaladas en el calendario de la universidad.</p>
              </div>
            </div>                
            
