        <h3>Capítulo VI  -  De los programas de los cursos.</h3>
                     <section class="section-60 section-sm-60 bg-lightest">
          <div class="shell text-sm-left">
            <div class="range range-xs-center">
            </div>
            
              <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 34</div>
                <div>
                  <h5 class="text-bold">Programas de los cursos.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Todo curso que se imparta en la Universidad Tecnológica Costarricense, contará con el programa correspondiente, el cual será entregado a los estudiantes por el profesor, el primer día de lecciones.</p>
              </div>
            </div>
            
      <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 35</div>
                <div>
                  <h5 class="text-bold">Actualización de programas</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Los programas serán revisados y actualizados periódicamente, con el propósito de responder a las expectativas de los estudiantes y a los requerimientos del desarrollo nacional.<br> Las Direcciones de Carrera llevarán un archivo de los Planes de Estudio y de los programas de los cursos bajo su administración.  Este archivo incluirá los planes y programas anteriores vigentes.
</p>
              </div>
            </div>   
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 36</div>
                <div>
                  <h5 class="text-bold">Conocimiento del alcance y contenido de programas</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>El contenido de cada curso está explícito en el programa que se entrega a los estudiantes, y cuya conformación debe ser ampliamente conocida y manejada por el profesor.  Las actividades docentes de los profesores, estarán sujetas por el programa académico  del curso, al que deben ser fieles en su aplicación.  No obstante, los profesores podrán presentar al Director de Carrera correspondiente, las recomendaciones del caso para variar sus actividades, en procura del mejoramiento y actualización del programa, pero efectuarán dichas variaciones solo cuando hayan recibido el visto bueno por escrito de la máxima autoridad universitaria.</p>
              </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 37</div>
                <div>
                  <h5 class="text-bold">Información que deben incluir los programas</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>En todos los cursos, el profesor tiene la obligación de facilitar a cada estudiante el programa académico que incluirá, por lo menos,  la siguiente información: nombre, código y descripción del curso; objetivos generales y específicos, desglose de contenidos, metodologías y técnicas de enseñanza, fechas de examen y procedimiento de evaluación; trabajos especiales, particularmente los de aplicación, textos de lectura obligatoria, bibliografía de consulta, bibliografía final.
</p>
              </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 38</div>
                <div>
                  <h5 class="text-bold">Procedimiento para modificar programas.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Los programas de los cursos serán los aprobados por CONESUP, no obstante, cuando ameriten cambios, serán recomendados al Director de Carrera con suficiente antelación, por el cuerpo de profesores que conforman el área de atención del curso; el Director los estudiará y los aprobará o improbará y los pasará a conocimiento de la Rectoría, la que los enviará al CONESUP para su aprobación definitiva.<br>Los programas deberán revisarse en forma crítica por lo menos cada dos años, con el fin de mantener la calidad académica.  Para ello, la Rectoría solicitará a los Directores de carrera el estudio respectivo, y ellos, en un plazo no mayor de tres meses, deberán rendir un informe.  Cada Director llevará un archivo de los programas de los cursos bajo su administración, para consulta de los estudiantes u otros efectos. </p>
              </div>
            </div>              
  <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 39</div>
                <div>
                  <h5 class="text-bold">Exigencia de libros de texto</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Ningún profesor podrá exigir a sus alumnos, libros de texto que no se encuentren disponible en número suficiente en el mercado nacional.  En su defecto, el profesor tiene la obligación de procurar que la Universidad mantenga en servicio de reserva, en número adecuado de tales textos, para el desarrollo normal del curso.<br>En casos comprobados de carencia total o parcial de la bibliografía sobre la materia de un curso, el profesor que lo imparte podrá elaborar antologías o compilaciones de temas seleccionados para tal fin, las cuales se someterán previamente a la aprobación del Director de Carrera correspondiente, con el propósito de ponerlas a disposición inmediata de los estudiantes, por los medios mas convenientes y accesibles.
</p>
              </div>
            </div>      
