        <h3>Capítulo VII  -  De la Evaluación del Aprovechamiento.</h3>
          <section class="section-60 section-sm-60 bg-lightest">
          <div class="shell text-sm-left">
            <div class="range range-xs-center"></div>
            
              <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
              <div class="text-bold text-primary">Artículo 40</div>
              <div>
              <h5 class="text-bold">Distintas formas de evaluación</h5>
              </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>La evaluación del aprovechamiento se hará durante el transcurso de cada ciclo lectivo, mediante la aplicación de pruebas parciales, finales, extraordinarias, de reposición, tareas, exámenes cortos, trabajos de investigación, exposiciones, asignaciones especiales, asistencia a clases y cualquiera otra forma válida que permita determinar el aprovechamiento, según la naturaleza de la materia. Habrá tres periodos de evaluación en la quinta, décima y decimocuarta semana del ciclo lectivo, sin detrimento de cualquier ajuste que requiera el calendario universitario, en virtud de acontecimientos coyunturales.</p>
              </div>
            </div>
            
      <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 41</div>
                <div>
                  <h5 class="text-bold">Escala de calificación.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>La calificación es numérica y se hará en la escala de 1 a 100.</p>
              </div>
            </div>   
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 42</div>
                <div>
                  <h5 class="text-bold">Porcentaje requerido para aprobar un curso.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Para aprobar un curso tanto de Bachillerato como de Licenciatura, el estudiante debe alcanzar  un promedio mínimo de 70%; salvo en los trabajos finales de graduación, Pruebas de Grado y Tesis;  para los que se requiere un promedio mínimo de 80%. Igual porcentaje (80%) se requiere para los cursos de Maestría.  Las calificaciones se basarán prioritariamente en la siguiente distribución porcentual, como valor máximo de cada una de las categorías propuestas:
<ul>
	<li>Exámenes escritos, de ejecución y orales		 60%
	<li>Tareas, pruebas cortas, exposiciones, 
<li>Asignaciones, trabajos de investigación		           30%
<li>Participación en clase				 	10%
</li>
</ul>
En cada curso, y de común acuerdo entre el profesor y los estudiantes, se podrán cambiar los porcentajes específicos para cada examen, trabajos de investigación, tareas, pruebas cortas, exposiciones y otros. En tal caso el profesor notificará los cambios al Director de Carrera. 
</p>
              </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 43</div>
                <div>
                  <h5 class="text-bold">Otras disposiciones que norman la evaluación de cursos.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>La evaluación de cada curso, en los distintos grados y posgrados, se regirá por las siguientes normas:
<ul>
<li>a)	Los resultados de los diferentes tipos de evaluación se cuantifican con base en la escala de 1 a 100.
<li>b)	 El estudiante que obtenga un promedio final de 1 a 59, deberá repetir el curso, pues su resultado es de reprobado.
<li>c)	El estudiante que obtenga un promedio final de 60 a 69 , tiene derecho a un examen extraordinario.  Este examen deberá realizarse una semana antes de iniciar el siguiente cuatrimestre.  En el supuesto de que reprobase o se pase la fecha de convocatoria correspondiente, el estudiante deberá cursar de nuevo la materia.  En caso de realizar el examen extraordinario, el estudiante debe cancelar el arancel correspondiente, al menos con 48 horas de antelación a la fecha del examen.  Estas calificaciones rigen tanto para estudiantes de Bachillerato y Licenciatura.
<li>d)	Los Seminarios de Investigación, en cualquiera de los grados académicos, se aprueban con calificación no menor de 70.  En caso de obtener una calificación inferior, el estudiante repetirá el seminario, previa matrícula y cancelación de los derechos correspondientes.
<li>e)	La calificación correspondiente a la aprobación de un examen extraordinario es de 70.
<li>f)	Las Tesis y Pruebas de Grado se aprueban con 80 no existiendo posibilidad de nueva defensa en el primer caso; o de examen extraordinario en las Pruebas de Grado. Las Pruebas de Grado por su parte serán aprobadas mediante prueba escrita, con un valor del 60%, y prueba oral, con un valor del 40%; en este último caso la Rectoría nombrará un Tribunal Examinador integrado por el propio Rector o el Director o Coordinador de Carrera (o su representante)  y dos profesionales en el área a examinar. 
</li>
</ul>
</p>
              </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 44</div>
                <div>
                  <h5 class="text-bold">Recursos de revocatoria</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Si el estudiante se muestra disconforme con el promedio final, tiene la posibilidad de presentar un recurso de revocatoria debidamente fundamentado ante el profesor del curso, en un plazo no mayor de 3 días hábiles, después de entregado el resultado.  El profesor debe pronunciarse sobre el recurso, dentro de los 5 días hábiles siguientes.<br>
Si el estudiante disiente con respecto a la resolución otorgada por el profesor, podrá interponer un recurso de apelación dentro de los ocho días calendario, ante el Director de Carrera, quién tendrá cinco días hábiles para pronunciarse y definir al estudiante su recurso dictando el acto final.
 </p>
              </div>
            </div>              
  <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 45</div>
                <div>
                  <h5 class="text-bold">Informe de calificaciones.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>El docente  tiene la obligación de entregarle el promedio final a cada estudiante, antes de presentarlo al Departamento de Registro, mediante el acta correspondiente y, a más tardar, ocho días calendario después de terminado el período correspondiente.</p>
                </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 46</div>
                <div>
                  <h5 class="text-bold">Requisitos al practicar una evaluación</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>En cualquier tipo de evaluación que se practique, el alumno deberá cumplir con los siguientes requisitos:
                <ul>
<li>a)	Presentar el carné o cédula que lo acredite como estudiante de la Universidad, y que le permita su correcta identificación.
<li>b)	Ingresar al recinto donde realizará el examen a la hora prefijada, permitiéndole en casos de excepción, a juicio del profesor, una demora máxima de 15 minutos.
<li>c)	Si el profesor no se presenta a la hora prefijada, ni ha notificado su retraso, los estudiantes deben permanecer en el aula un máximo de 15 minutos; transcurridos los cuales notificarán a las autoridades universitarias correspondientes para que levanten un acta de asistencia; 
<li>d)	Firmar una constancia de asistencia al finalizar el examen, junto con la entrega del mismo.
<li>e)	Realizar la prueba en el grupo en que se encuentra matriculado.
<li>f)	Encontrarse al día con el pago de aranceles universitarios, lo que se demostrará con la presentación del recibo de comprobante de pago.
<li>g)	Cualquier otro requisito que la Universidad considere conveniente, según el proceso evolutivo de la misma. </li>
 <ul>
 </p>
  </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 47</div>
                <div>
                  <h5 class="text-bold">Cuadernos para examen</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Para realizar las evaluaciones, los estudiantes traerán sus cuadernos oficiales en blanco y sin nombre, para entregarlos al profesor antes de iniciar la evaluación, quien una vez que haya comprobado que están en orden, los devolverá de inmediato a los estudiantes.  Practicado en examen, el profesor lo devolverá al estudiante debidamente revisado, a más tardar  8 días calendario contados a partir de la fecha en que se realizó la evaluación.<br>

Dependiendo de la especialidad de la carrera, el profesor podrá solicitar al estudiante otro tipo de material como: libros, calculadora, entre otros, para la elaboración de las pruebas, el cual debe requerirse con antelación a la fecha del examen.<br>

Los estudiantes deben tener sobre el pupitre, al momento de inicio del examen, todos los materiales necesarios para la elaboración del mismo, conforme las indicaciones del profesor.  Durante el examen no se permite sacar nada, ni prestar o pedir prestado material a otro compañero, a menos que el profesor así lo indique expresamente

 </p>
  </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 48</div>
                <div>
                  <h5 class="text-bold">Consultas durante los exámenes</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Una vez distribuidas las pruebas de evaluación, el profesor otorgará un periodo de 15 minutos para evacuar consultas, las cuales  deben ser únicamente dudas de forma, no de contenido.  Las preguntas que se realicen durante ese lapso, deben formularse en voz alta y desde el pupitre del estudiante, después de que el profesor le conceda la palabra.

 </p>
     </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 49</div>
                <div>
                  <h5 class="text-bold">Abandono del recinto mientras se realiza una prueba.</h5>
                </div>
              </div>
              <div class="cell-xs-10 cell-sm-8 offset-top-14 offset-sm-top-0">
                <p>Una vez que la prueba haya iniciado, no se permitirá al alumno, bajo ninguna circunstancia, excepto caso fortuito o fuerza mayor, abandonar el recinto de examen sin haber finalizado la prueba.

 </p>    
          </div>
            </div>      
            <div class="range range-xs-center offset-top-50">
              <div class="cell-sm-4">
                <div class="text-bold text-primary">Artículo 50</div>
                <div>
                  <h5 class="text-bold">Fraudes durante y después del examen.</h5>
