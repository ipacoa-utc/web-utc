Se aplicará suspensión por un mes al estudiante que:
<ul>
<li>	Se presente a una sesión de práctica dirigida, exámenes o cualquier actividad académica de la Universidad o a cualquier dependencia universitaria, bajo los efectos de drogas, estupefacientes o bebidas alcohólicas, portando armas sin estar autorizado o haciendo uso indebido de ellas...
<li>	Agreda de palabra o de hecho a un miembro del personal docente o administrativo de la Universidad o a otro estudiante.
<li>	Observe una conducta inmoral.
<li>	Reincida en una conducta inmoral o escandalosa dentro de la Universidad.
</ul>
Se aplicará expulsión de la Universidad al estudiante que:
  <ul>
<li>	Reincida al presentarse a una sesión de práctica dirigida, exámenes o cualquier actividad académica o dependencia universitaria, bajo los efectos de las drogas, estupefacientes o bebidas alcohólicas.
<li>	Falsifique firmas o documentos de carácter universitario, o para uso de la Universidad.
<li>	Reincida en una conducta inmoral o escandalosa dentro de la Universidad.
<li>	Robe o destruya libros o cualquier material bibliográfico y equipo, y prive a otros del acceso a estos documentos.  Esto constituye una violación a la integridad académica.
<li>	Entrega de títulos falsos.
